    ArrayList<Car> cars;
    public MainFormWithPanel() {
         Button exitButton = new Button("Exit");
         Button addCarButton = new Button("Add Car");
         //addCarButton
         exitButton.setPreferredSize(new Dimension (30,15));
         //setLayout(new GridLayout(0,4));//set rows to zero so it fills rows first
        
        InventoryFileReader reader = new InventoryFileReader();
        
        ArrayList<Car> cars = reader.getAllCars();
        JPanel comboPanel = new JPanel();
        JPanel buttonsPanel = new JPanel();
        buttonsPanel.setLayout(new GridLayout(0,2));
        comboPanel.setLayout(new GridLayout(0,2));
        
       comboPanel.add(exitButton);
       comboPanel.add(addCarButton);
        
        for(int i=0; i < cars.size(); i++){
            InventoryItemPanel panel = 
                    new InventoryItemPanel(cars, cars.get(i));
            comboPanel.add(panel);
            
        }
        
         JScrollPane scrollPane = new JScrollPane(comboPanel, ScrollPaneConstants.VERTICAL_SCROLLBAR_AS_NEEDED, ScrollPaneConstants.HORIZONTAL_SCROLLBAR_NEVER);
        scrollPane.setPreferredSize(new Dimension(920,500));
         
        add(scrollPane);
         //add(comboPanel);
        
        this.cars = cars;
      
         
         pack();
          exitButton.addActionListener(new java.awt.event.ActionListener() {
            public void actionPerformed(java.awt.event.ActionEvent evt) {
                jButton10ActionPerformed(evt);
            }
        });
          
          addCarButton.addActionListener(new java.awt.event.ActionListener() {
            public void actionPerformed(java.awt.event.ActionEvent evt) {
                jButton11ActionPerformed(evt);
            }
        });
        
        //add a loop
        
        pack();
        
                 

        
        initComponents();
  
        
    }
    
    

